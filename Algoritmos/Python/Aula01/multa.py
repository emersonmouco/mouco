
"""
Desenvolva um algoritmo que pergunte a velocidade
do carro de um usuario, se a velocidade for 
acima de 90 km será multado, se nao, nao.
"""
velocidade = float(input("Informe a velocidade do carro: "))

if velocidade > 90: 
    print("O veículo será multado!")
else:
    print("O veículo está dentro do limite de velocidade!")
    
    
    
    