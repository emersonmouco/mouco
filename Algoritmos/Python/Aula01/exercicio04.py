# Desenvolva um algoritmo que leia o lado de 
# um quadrado e calcule sua area, mostre depois
# a area do quadrado e o seu dobro e triplo

lado = float(input("Informe o lado do quadrado: "))

area = lado * lado
dobro = area * 2
triplo = area * 3
print("A área do quadrado é igual a: ",area)
print("O dobro da área é: ",dobro)
print("O triplo da área é: ",triplo)