"""
faça um algoritmo que calcule o salario
do trabalhador. Ou seja, a pessoa deverá
informar a quantidade de horas trabalhadas
e o valor da hora trabalhada.
Depois calcule e mostre o salário final
"""
quantidade = float(input("Informe a quantidade de horas trabalhadas: "))
valor_hora = float(input("Informe o valor da hora trabalhada: "))

salario = quantidade * valor_hora

print(f"O valor final do salário será de R$ {salario}")












