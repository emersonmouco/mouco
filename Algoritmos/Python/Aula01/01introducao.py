
# comentário de uma linha é com hashtag #
""" comentário com mais de uma linha
sao tres aspas duplas seguidas para abrir
e depois tres para fechar """
print(123)
print("Emerson Mouco")
print(1+1)
print("Emerson \n Mouco")
print("1+1")

print(12, 34, 1011, sep="",end="#")
print(12, 25, 1234, sep="-", end="\n")
print(25, 3, 1994, sep="\t", end="\n")

